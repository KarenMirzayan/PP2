# From 2nd to 5th letter, counting from 0 and 5 not included
b = "Hello, World!"
print(b[2:5])

# From start to 5th, 5 not included
b = "Hello, World!"
print(b[:5])

# From 2nd to end, counting from 0
b = "Hello, World!"
print(b[2:])

#From 5th from end to 2nd from end, 2nd from end not included
b = "Hello, World!"
print(b[-5:-2])